    <!-- Simple countdown script -->
    <script>
        // Set the date we're counting down to (30 days from now)
        const countDownDate = new Date();
        countDownDate.setDate(countDownDate.getDate() + 30);
        
        // Update the countdown every 1 second
        const x = setInterval(function() {
            // Get current date and time
            const now = new Date().getTime();
            
            // Find the distance between now and the countdown date
            const distance = countDownDate - now;
            
            // Time calculations for days, hours, minutes and seconds
            const days = Math.floor(distance / (1000 * 60 * 60 * 24));
            const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
            const seconds = Math.floor((distance % (1000 * 60)) / 1000);
            
            // Display the result
            document.querySelector('.countdown div:nth-child(1) span:first-child').textContent = 
                days.toString().padStart(2, '0');
            document.querySelector('.countdown div:nth-child(2) span:first-child').textContent = 
                hours.toString().padStart(2, '0');
            document.querySelector('.countdown div:nth-child(3) span:first-child').textContent = 
                minutes.toString().padStart(2, '0');
            document.querySelector('.countdown div:nth-child(4) span:first-child').textContent = 
                seconds.toString().padStart(2, '0');
            
            // If the countdown is finished, display some text
            if (distance < 0) {
                clearInterval(x);
                document.querySelector('.countdown').innerHTML = "LAUNCHING TODAY!";
            }
        }, 1000);
    </script>
